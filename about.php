   SULS as a variety talk show has performed at The Empty Space, ConWorks, Capitol Hill Arts Center (CHAC), Balagan Theatre, Buster's at ACT, and The Jewel Box @ The Rendezvous. At <a href="http://www.theatreoffjackson.org" target="_blank">Theatre Off Jackson</a>, SULS co-produced a full-length, theatrical premiere, <em>Girl You Know It's True</em> directed by the talented Ed Hawkins. At this point, our mission statement became more refined:</p>
<blockquote>
    The Stay Up Late Show (SULS) is a theatrical producing body focused on stimulating older artists to make new work. In addition, The Stay Up Late show produces its one-of-a-kind LIVE talk show/cabaret on a quarterly basis, bringing together the best of theater, burlesque, arts, politics, and often local cuisine.
</blockquote>
